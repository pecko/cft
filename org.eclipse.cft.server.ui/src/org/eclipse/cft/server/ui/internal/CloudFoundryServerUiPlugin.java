package org.eclipse.cft.server.ui.internal;

import org.eclipse.core.runtime.IStatus;
import org.eclipse.core.runtime.Status;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * @author Christian Dupuis
 */
public class CloudFoundryServerUiPlugin extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "org.eclipse.cft.server.ui"; //$NON-NLS-1$

	private static CloudFoundryServerUiPlugin plugin;

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	public static CloudFoundryServerUiPlugin getDefault() {
		return plugin;
	}

	public static void logError(Throwable e) {
		if (plugin != null) {
			plugin.getLog().log(new Status(IStatus.ERROR, PLUGIN_ID, e.getLocalizedMessage()));
		}
	}
	
	public static void logWarning(Throwable e) {
		if (plugin != null) {
			plugin.getLog().log(new Status(IStatus.WARNING, PLUGIN_ID, e.getLocalizedMessage()));
		}
	}

}
